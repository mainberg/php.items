					<p>1998 - 2001 Interreg II Alpenrhein-Bodensee-Hochrhein, Projekt 147</p> 
					<h1>Modernisierung und Vernetzung der Bibliotheken in der
					Bodenseeregion</h1>


					<p>Das Projekt hat zum Ziel, die Kataloge der beteiligten
					Bibliotheken, soweit sie noch nicht in maschinenlesbarer Form
					vorliegen, in eine solche zu überführen. Insbesondere sollen
					diejenigen Zettelkarten konvertiert werden, deren Titel einen Bezug
					zur Regio Bodensee in historischer, wirtschaftlicher, kultureller und
					naturwissenschaftlicher Hinsicht haben. Dadurch werden die
					Voraussetzungen für den "Elektronischen Bodenseekatalog" geschaffen.</p>


					<p>Forschung, Lehre, Studium, berufliche Tätigkeit und
					Fortbildung, allgemeine Information sowie die Beschäftigung mit
					Geschichte und Kultur des Bodenseeraumes erfordern den Zugriff auf die
					in den Bibliotheken aufbewahrte Literatur. Die Bodenseeregion ist
					reich an Universitäts- und Hochschulbibliotheken, größeren und
					kleineren Landes-, Stadt- und Spezialbibliotheken mit lokal, regional
					und überregional bedeutsamen Beständen. Den Benutzern der
					verschiedenen Bibliotheken soll der Zugang zu allen Wissenspeichern
					der Regio Bodensee durch die Nutzung der Möglichkeiten der
					Internettechnologie erleichtert werden.</p>

					<p>Ein Interessent wird sich von seinem Arbeitsplatz aus, sei
					dieser in der Bibliothek oder zu Hause, schnell und bequem darüber
					informieren können, welche Bibliotheken die benötigten Einzeltitel
					oder allgemein Literatur zu einem bestimmten Thema besitzen. Er erhält
					Informationen über deren Benutzbarkeit, Öffnungszeiten,
					Ausleihmodalitäten, eventuell die Möglichkeit zur Bestellung bzw.
					Vormerkung per Email. Kopien von Aufsätzen sollten - gegen
					Kostenerstattung - direkt per Post, Fax oder Email zugestellt werden.
					Bücher können - wie bisher auch - über den Fernleihverkehr der
					Bibliotheken beschafft werden.</p>

					<p>Auf diese Weise können viel Zeit und Kosten gespart werden, die
					für Fahrten zu den Bibliotheken anfielen, ohne daß ein Erfolg absehbar
					war. Bibliotheksbesuche können nun systematischer vorbereitet, die
					nächstgelegene Bibliothek gezielt ausgewählt oder ein lohnender
					längerer Bibliotheksbesuch geplant werden. Vielleicht ist die eigene
					Bibliothek auch einem Anschaffungsvorschlag zugänglich, wenn sich
					herausstellt, daß ein Werk von fast allen anderen gekauft wurde und
					anscheinend zum Standard gehört.</p>

					<p>Die Bibliotheken der Bodenseeregion beschlossen, daß die
					Benutzer einer Bibliothek an den anderen Zugang zu den gleichen
					Bedingungen haben sollen wie die Leser vor Ort.</p>

					<p>Das Projekt ist zweistufig angelegt. Zunächst soll die
					Nachweissituation in den einzelnen Bibliotheken verbessert werden. Das
					heißt, daß entweder Zettelkataloge in den elektronischen Katalog zu
					überführen sind oder bisher nicht erfaßte Bestände endlich
					katalogisiert werden. Alle Bibliotheken führen ihre EDV-Kataloge
					weiter wie bisher.</p>
					<p>In einem zweiten Schritt will die Universität Konstanz die
					bibliographischen Daten und Besitznachweise der einzelnen
					Bibliothekskataloge unter einer Oberfläche anbieten und mittels einer
					noch zu programmierenden Suchmaschine gemeinsam durchsuchbar machen.</p>
					
