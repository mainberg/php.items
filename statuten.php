					<h1>Verein Bibliotheken der Regio Bodensee</h1>

					<h2>Statuten</h2>
					
					<h3>A. Name, Sitz, Zweck</h3>
					
					<p>§ 1 Unter dem Namen Bibliotheken der Regio
					Bodensee besteht ein Verein im Sinn von Art. 60ff. ZGB mit Sitz in
					St.Gallen.</p>
					
					<p>§ 2 Der Verein fördert den Austausch und die
					grenzüberschreitende Zusammenarbeit unter den Bibliotheken der
					Bodenseeregion.</p>
					
					<h3>B. Mitgliedschaft</h3>
					
					<p>§ 3 Mitglieder des Vereins können Bibliotheken
					der Regio Bodensee werden, sei es als juristische Personen oder als
					Gesellschaften ohne Rechtspersönlichkeit. Aufgenommen werden sie durch
					Beschluss des Vorstands; dieser kann die Aufnahme ohne Angabe von
					Gründen verweigern.</p>
					
					<p>§ 4 Der Verein erhebt von den Mitgliedern
					einen Jahresbeitrag. Die Mitgliederversammlung legt dessen Höhe fest.
					Der Vorstand kann einzelne Mitglieder ganz oder teilweise von der
					Beitragspflicht entbinden.</p>
					
					<p>§ 5 Nach schriftlicher Anzeige an den Vorstand
					kann ein Mitglied auf Ende Dezember aus dem Verein austreten. Der
					Vorstand kann Mitglieder ohne Angabe von Gründen aus dem Verein
					ausschliessen. Das zweimalige Nichtbezahlen des Jahresbeitrags gilt
					als Ausschlussgrund.</p>
					
					<h3>C. Organisation</h3>

					<p>§ 6 Organe des Vereins sind:</p>
					
					<ol>
						<li>die Mitgliederversammlung</li>
						<li>der Vorstand</li>
						<li>zwei Rechnungsrevisorinnen oder Rechnungsrevisoren</li>
					</ol>
					
					<p>§ 7 Die Mitgliederversammlung hat die folgenden Befugnisse:</p>
					
					<ol>
						<li>Wahl der Präsidentin oder des Präsidenten und der übrigen
						Vorstandsmitglieder</li>
						<li>Genehmigung des Protokolls der Mitgliederversammlung, des
						Jahresberichts, der</li>
						<li>Jahresrechnung und des Revisionsberichts</li>
						<li>Festsetzung des Jahresbeitrags</li>
						<li>Beschluss über Anträge</li>
						<li>Beschluss über die Revision der Statuten</li>
						<li>Beschluss über Auflösung des Vereins.</li>
					</ol>
					
					<p>§ 8 An der Mitgliederversammlung sind die
					anwesenden Mitglieder mit je einer Stimme stimmberechtigt. Die
					Mitgliederversammlung beschliesst wenn nicht anders vorgesehen mit dem
					einfachen Mehr der gültig abgegebenen Stimmen.</p>
					
					<p>§ 9 Zusätzlich zur jährlichen
					Mitgliederversammlung werden nach Bedarf weitere Konferenzen der
					Mitglieder durchgeführt, zu denen der Vorstand einlädt.</p>
					
					<p>§ 10 Der Vorstand besteht aus drei bis fünf
					Mitgliedern: der Präsidentin oder dem Präsidenten, der Schriftführerin
					oder dem Schriftführer, der Kassierin oder dem Kassier sowie aus bis
					zu zwei Beisitzerinnen oder Beisitzern. Die Präsidentin oder der
					Präsident und die Mitglieder des Vorstands werden von der
					Mitgliederversammlung jeweils auf drei Jahre gewählt. Im übrigen
					konstituiert sich der Vorstand selbst.<br>
					Der Vorstand ist beschlussfähig, wenn mindestens die Hälfte der
					Mitglieder anwesend sind. Bei Stimmengleichheit hat die Präsidentin
					oder der Präsident den Stichentscheid. Die Beschlüsse werden
					protokolliert.</p>
					
					<p>§ 11 Der Vorstand vertritt den Verein nach
					aussen und führt dessen Geschäfte, soweit sie nicht in die
					Zuständigkeit anderer Organe fallen.</p>
					
					<p>§ 12 Die beiden Rechnungsrevisorinnen oder
					Rechnungsrevisoren werden von der Mitgliederversammlung auf jeweils
					drei Jahre gewählt. Sie prüfen die Buchhaltung und den
					Rechnungsabschluss des Vereins, erstatten der Mitgliederversammlung
					Bericht und stellen Antrag.</p>
					
					<h3>D. Haftung</h3>
					
					<p>§ 13 Für die Verbindlichkeiten des Vereins
					haftet ausschliesslich das Vereinsvermögen. Jede persönliche Haftung
					der Mitglieder ist ausgeschlossen.</p>
					
					<h3>E. Schlussbestimmungen</h3>

					<p>§ 14 Das Vereinsjahr beginnt am 1. Januar und
					endet am 31. Dezember.</p>
					
					<p>§ 15 Für die Auflösung des Vereins sind drei
					Viertel der gültig abgegebenen Stimmen notwendig. Die
					Auflösungsversammlung beschliesst über die Verwendung eines
					allfälligen Liquidationsüberschusses.</p>
					
					<p>Diese Statuten sind von der
					Gründungsversammlung am 18. Oktober 2006 in Dornbirn beschlossen
					worden. Sie treten sofort in Kraft.</p>
					
