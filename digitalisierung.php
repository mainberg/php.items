					<p>2008 - 2012 Interreg IV Alpenrhein-Bodensee-Hochrhein, Projekt 11</p> 
					
					<h1>Kulturelles Erbe der Regio Bodensee online</h1>

					<p>Bibliotheksbenutzer erwarten heute von Bibliotheken mehr als
					bloß über Internet abrufbare Katalognachweise für ihre Bestände.
					Gefordert wird die rasche und direkte Zugriffsmöglichkeit auf die
					Inhalte der Informationsträger. Das Anbieten von digitalisierten und
					damit online recherchierbaren Bibliotheksbeständen mit regionaler
					Bedeutung wäre in dieser Hinsicht zeitgemäss und auch im
					internationalen Zusammenhang (Google Print, Europeana/European Digital
					Library) naheliegend und sinnvoll. Gleichzeitig kann damit der
					Bodenseeraum als kulturelle Einheit zur Geltung gebracht werden.</p>

					<p>Beabsichtigt ist das Digitalisieren von ca. 400.000 Seiten
					regionaler Kernzeitschriften in der Form, dass die Volltexte der
					historischen Zeitschriften des Bodenseeraumes im Internet über zwei
					Zugangsmöglichkeiten präsentiert werden können: formaler Zugang über
					Titel, Heft, Seite (Blätterfunktion) sowie inhaltlicher Zugang über
					OCR/Volltextrecherche.</p>

					<p>Das Projekt erbringt zusätzliche Leistungen für die Bürger der
					Region, die allerdings in den nächsten Jahrzehnten als
					selbstverständlich angesehen werden. Die Entwicklung zur
					elektronischen Dienstleistung mit umfassendem digitalen Angebot
					befindet sich am Anfang. Das Projekt soll für die Regio Bodensee einen
					maßgeblichen Anstoß liefern zur Vermittlung von regionaler Geschichte
					und Kultur. Mit der Digitalisierung historischer Kernzeitschriften
					wird der Zugang zu der in mehreren hunderttausend Seiten gespeicherten
					Information wesentlich erleichtert.</p>

					<p>Die entscheidende Neuerung bei der Digitalisierung liegt darin,
					dass die Zeitschriften durch die OCR-Bearbeitung (Texterkennung) im
					Volltext recherchierbar werden, unabhängig davon ob sie in Fraktur
					oder in einer lateinischen Schrift gedruckt wurden. Erst damit
					entfaltet die Digitalisierung ihre volle Wirkung, werden die
					gespeicherten elektronischen Daten der Zeitschriften zur Fundgrube für
					den Forscher, dem sich diese Informationsquelle in diesem Ausmaß
					bisher nicht erschlossen hat.</p>
					
