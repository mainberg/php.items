<h2>28. März 2007, Bibliotheksservice-Zentrum Baden-Württemberg, Konstanz 
<br />
1. Mitgliederversammlung des Vereins Bibliotheken der Regio Bodensee</h2>
<p>
Mitgliederentwicklung: Der Verein startet mit 26 Mitgliedern.
<br />
Bericht des Präsidenten; Bericht des Kassiers; Festsetzung des Jahresbeitrags 2008.
<br />
Webseite: Der Verein möchte sich mit einem einfachen aber prägnanten Auftritt im Web präsentieren. Die Eingangsseite könnte wie folgt strukturiert sein: 
Charta des Vereins; Landkarte der Regio Bodensee mit den Standorten (Punkte) der beteiligten Bibliotheken; Kernangaben pro Bibliothek; 
Linkliste zu den aktuellen Bodenseeaktivitäten einzelner Bibliotheken.
<br />
Gewünscht wird ein eigenes Vereinslogo und ein erkennbarer grafischer Auftritt (Briefpapier); ergeht als Auftrag an den Vorstand. Recherchiert und 
reserviert wird der Domain-Name «Bodenseebibliotheken» mit allen möglichen Endungen [de, at, ch, li, eu].
<br />
Digitalisierungsprojekt: In nächster Zeit wird der Vorstand einen formlosen Erstantrag (Projektentwurf) bei der zuständigen Stelle für Interreg-Programme einreichen. 
<br />
Christof Mainberger präsentiert ein Exposé des BSZ zur technischen Infrastruktur des Projekts «Digitalisierte Bodensee-Zeitschriften». 
Die Bedeutung der Bodenseebibliographie als Bezugsdatenbank wird besonders hervorgehoben. Sie ermöglicht den Zugang zu einzelnen Artikeln. 
Für die Lieferung bibliographischer Datensätze unveröffentlichter Artikel ist die einzelne Bibliothek zuständig. 
<br />
Erörterung, wie es mit dem Bodenseekatalog weiter gehen soll und ob eine Verlinkung des Katalogs mit der Bibliographie sinnvoll wäre. 
<br />
Katastrophenplanung: In vielen Bibliotheken fehlt eine systematische und festgehaltene Planung zum Umgang mit Katastrophen (Brand, Wasser). 
Es ist geplant, sich an der Herbstsitzung unter Beiziehen von Experten aus St. Gallen mit diesem Thema näher auseinanderzusetzen. 
<br />
Herr Uhrmann, Dr. Erich-Bloch-Bibliothek in Konstanz, bekundet sein Interesse an einer Mitgliedschaft und stellt kurz die Bibliothek vor.
<br />
Abschließend Mitteilungen aus den Bibliotheken.
</p><br />

