<h2>23. Februar 2016, Universität Konstanz
<br />
10. Mitgliederversammlung und Frühjahrstagung</h2>
<p>
<b>Teil 1: Vereinsagenden</b>
<br />
Begrüßung der 30 Teilnehmerinnen und Teilnehmer durch den Präsidenten und Oliver Kohl-Frey, Vizedirektor der UB Konstanz, insbesondere 
die Vertreter von Bibliotheken der Internationalen Bodenseehochschule (IBH), die noch nicht Mitglied des Vereins sind, und deren 
Geschäftsführer, Stephan Prehn. Die gemeinsame Veranstaltung soll dem Austausch und dem Netzwerken innerhalb der Groß-Region Bodensee dienen. 
<br />
Mitgliederentwicklung: Weiterhin sind 32 Institutionen Mitglied.
<br />
Die Tagungschronologie ist zurück bis zur Vereinsgründung 2006 komplettiert und auf der Website verfügbar. Die Kooperation geht 
sicher bis in die 1970er Jahre zurück, intensiviert durch das Projekt Bodenseebibliographie seit den 1970/80er Jahre. Der 
Präsident dankt Schriftführer, Kassier und Revisoren für ihren Einsatz, den Tagungsorganisatoren für Bereitschaft und Engagement 
und dem BSZ für die gute Pflege des Portals. Er appelliert daran, dessen Inhalte aktuell zu halten, insbesondere News und 
Veranstaltungen, und die Bodensee-Links nach möglichen Ergänzungen hin anzusehen. 
<br />
Kassenbericht und Revisionsbericht: Die Versammlung verabschiedet die Rechnung einstimmig und ohne Enthaltungen.
<br />
Der Jahresbeitrag bleibt unverändert.
<br />
Jahresbericht der Bodenseedatenbank (Oliver Trevisiol): In der Datenbank sind inzwischen fast 103.000 Titelnachweise vorhanden.
<br />
Webportal <a href="http://www.bodenseebibliotheken.eu/">www.bodenseebibliotheken.eu</a>: Seit der Aufschaltung der Volltexte im 
Zeitschriftenportal haben die Zugriffe rasant zugenommen und halten sich auf hohem Niveau, ein Beleg für die hohe Relevanz der 
Titel und Ansporn, die Digitalisierung weiterzuführen. Es wurden erstmalig über eine Million Zugriffe registriert, die meisten 
(87 %) erwartungsgemäß bei den Zeitschriften. Während bei den Zugriffen ein Plus von ca. 10 % zu verzeichnen ist, ist das Plus 
bei den Besuchen nur ca. 3 % (jetzt: 46.544). Die Tendenz der vergangenen Jahre setzt sich also fort: Die Webseiten werden von 
einem überschaubaren Kreis von Besuchern sehr ausgiebig genutzt. 
<br />
Berichte aus den Bibliotheken: Cornel Dora weist auf die neue Ausstellung der Stiftsbibliothek St. Gallen hin: Abacadabra, 
Medizin im Mittelalter. Petronella Däscher berichtet, dass die Kantonsbibliothek Graubünden ein Nachweis-Portal für AV-Medien 
aufgeschaltet hat. Oliver Thiele lädt die Teilnehmer ein, das englische E-Book-Portal der Bibliotheken Schaffhausen zu testen 
(Overdrive, kostenlose Nutzung für eingeschriebene Bibliothekskunden). 
<br /><br />
<b>Teil 2: Koordinationsbesprechung IBH / Bodenseebibliotheken</b>
<br />
Stephan Prehn, Geschäftsführer der Internationalen Bodenseehochschule, und Harald Weigel bekräftigen den Willen zur engeren 
Zusammenarbeit und sehen gute Möglichkeiten für Austausch und Information wie auch für Kooperationsprojekte. 
Harald Weigel stellt den anwesenden IBH-Bibliotheken den 2006 gegründeten Verein vor. Bibliotheken sind teils durch ihre 
Trägereinrichtung Mitglied der IBH, teils aber auch zugleich Mitglied des Vereins. Die Mehrzahl im Verein hat eher oder zumindest 
auch einen geisteswissenschaftlichen oder landeskundlichen Hintergrund, ist auf diesem Gebiet historisch/bestandserhaltend 
interessiert, die Mehrzahl der Nicht-Vereinsbibliotheken der IBH wohl eher vorwiegend an Strategien aktueller Informationsversorgung 
für Lehre und Studium. Nicht-IBH-Bibliotheken tragen dabei auch maßgeblich zur Literatur- und Informationsversorgung der Regio 
und auch der Hochschulen bei. Bei aller Individualität der Bibliotheken hat sich ein schönes Zusammengehörigkeitsgefühl entwickelt, 
es wurden immer wieder Projektideen wie die gemeinsame Retrokatalogisierung und ein gemeinsamer Bodenseekatalog umgesetzt; nicht 
realisierbar, aber angestrengt betrieben worden, war ein gemeinsamer Bibliotheksausweis, ein gemeinsames Logo und durchschlagende 
öffentlichkeitswirksame Aktionen. Besonders hebt er das große Kooperationsprojekt des Vereins hervor, die Digitalisierung von 
landeskundlichen Zeitschriften der teilnehmenden Bibliotheken, zugänglich auf der Website 
<a href="http://www.bodenseebibliotheken.eu/">www.bodenseebibliotheken.eu</a>. (.de, .ch, .at, .li). 
Gegründet wurde der Verein gerade auch, um die Ergebnisse von Projekten später in der Routine am Leben erhalten zu können.
Die halbjährlichen Tagungen dienen dem Austausch und Networking sowie der Weiterbildung. Ein Beitritt von Bibliotheken der IBH wäre 
gerade in diesem Kontext sehr willkommen und würde den Kreis der Bodenseebibliotheken erfreulich vertiefen und erweitern. Themen der 
früheren Tagungen waren z. B. Katastrophenschutz und vieles Fachspezifische wie RDA, Bibliotheksgesetzgebung, Bibliotheksmanagement, 
EDV-Systeme – wozu auch externe Experten eingeladen wurden.
<br /> 
Schon vor Jahrzehnten wurde beschlossen: Jeder Einwohner der Regio hat zu den ortsüblichen Bedingungen Zugang zu allen Bibliotheken. 
Die Website gibt weiteren Aufschluss.
<br /><br />
In der Diskussion wird der Aspekt des Austauschs und der gegenseitigen Information sehr hervorgehoben.
<br />
Weitere angesprochene Themen:
<ul>
<li>Vereinbarung zwischen der IBH und dem Verein? Die Rolle der Nicht-IBH-Bibliotheken für den Hochschul- bzw. Bildungsbereich ist 
bisher nicht deutlich gemacht. Die einfachste Lösung zur Intensivierung der Zusammenarbeit wäre: Möglichst viele 
Bibliotheken treten dem Verein bei.</li>
<li>Wie kann die gegenseitige Unterstützung bei der Informationsversorgung der Nutzer noch verbessert werden?</li>
<li>Auf der Website zur Orientierung für die Interessenten eine Übersicht zu den Forschungs- und Sammelschwerpunkten der einzelnen Bibliotheken?</li>
<li>Wieder eine gemeinsame Suche in allen Katalogen ermöglichen? Die Umsetzungsmöglichkeiten werden allerdings kontrovers beurteilt.</li>
<li>Sollte ein Arbeitskreis Informationsvermittlung, neue Technologien, Koordinations- bzw. Vernetzungsstrategien etc. eingerichtet werden?</li>
<li>Das Thema Hochschul-Repositorien ist von Interesse. Als Kontaktperson stellt sich Helene Heller-Künz, FH Vorarlberg, zur Verfügung.</li>
<li>Einrichtung eines Bodensee-Wiki?</li>
<li>Verstärkte Öffentlichkeitsarbeit wie?</li>
<li>Auf den Tagungen sollte mit kurzen Bibliotheksporträt-Vorträgen das Wissen über die Arbeit der anderen und der Informationsaustausch 
insgesamt gestärkt werden.</li>
</ul>
Anschließend an die Sitzung führte Oliver Kohl-Frey durch die wieder eröffnete UB Konstanz
<br /><br />
[In den Wochen nach der Sitzung sind fünf IBH-Bibliotheken dem Verein beigetreten.]
</p><br />

