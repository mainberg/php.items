					<h1>Web-Infrastruktur für das digitalisierte Kulturerbe der
					Regio Bodensee</h1>

					<p>Die kooperative Arbeit der Bibliotheken für die Bevölkerung der
					Regio Bodensee soll auf einer gemeinsamen Plattform im Internet
					dargestellt werden. Für diese Plattform bestand bisher keine
					Infrastruktur.</p>

					<p>Ziel ist die Darstellung der Bodenseebibliotheken und ihrer
					Angebote im Internet im Allgemeinen (Grundinformationen,
					Zugänglichkeit, Service, Kataloge, Bestände etc.) als gemeinsame
					Leistung für die Bevölkerung der Regio sowie die Einrichtung einer
					gemeinsamen Plattform für digital angebotene Inhalte durch die
					Bereitstellung der technischen Infrastruktur für Speicherung und
					Präsentation. Der Web-Auftritt der Bodenseebibliotheken und der
					Volltext- (Zeitschriften-) Server sollen beim
					Bibliotheksservicezentrum Baden-Württemberg in Konstanz installiert
					werden.</p>
					
					<p>Im korrespondierenden Projekt "Kulturelles Erbe der Regio
					Bodensee" sollen die Inhalte im Internet aufbereitet werden: Das
					gemeinsame Web-Angebot wird durch Digitalisate erweitert, in einer
					ersten Phase insbesondere durch die online im Volltext lesbaren und
					recherchierbaren historischen Kernzeitschriften.</p>

					<p>Durch den gemeinsamen Internetauftritt der wissenschaftlichen
					Bibliotheken kann der Bodenseeraum als kulturelle Einheit zur Geltung
					gebracht werden.</p>

					<p>Es ist insbesondere auch Server-Kapazität mit entsprechender
					Verwaltungs- und Präsentationssoftware für das elektronisch
					vermittelte Angebot an digitalisierten Inhalten zum kulturellen Erbe
					der Regio Bodensee zu schaffen.</p>

					<p>Diese zusätzliche Leistung für die Bürger der Region bringt
					auch in der elektronischen Welt die Gemeinsamkeit der Regio Bodensee
					zur Geltung. Die über die Infrastruktur zugänglich gemachten Inhalte
					stehen dann allen zur Verfügung - weltweit. Dadurch wird nicht nur in
					der Regio die Zugänglichkeit erleichtert, sondern im World Wide Web
					die Regio mit ihrem kulturellen Erbe präsentiert und auch beworben.</p>
					
